import java.util.ArrayList;
public class SwapTwoNumber {
    public static void main(String args[])
    {
        ArrayList<Float> swap=new ArrayList<Float>();
        swap.add(10f);
        swap.add(20f);

       System.out.println(swap.get(0)); 
       System.out.println(swap.get(1));

       System.out.println(swap.indexOf(10f)); 
       System.out.println(swap.indexOf(20f));

    }
}
